#include "3-calc.h"

/**
 * op_add - function to add two integers
 * @a: first integer
 * @b: second integer
 * Return: the sum of the two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to sutract two integer
 * @a: first integer
 * @b: second integer
 * Return: The result of the subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply two integer
 * @a: first integer
 * @b: second integer
 * Return: the result of the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of the divisions
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to find modulus
 * @a: first integer
 * @b: second integer
 * Return: the result of the modulus division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
